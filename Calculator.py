x = float(input("Whats your first number brah?? "))
y = float(input("Whats your second number?? "))
fn = input("What do you wanna use? +, -, /, *?? ")

if fn == "+":
    print(x+y)
elif fn == "-":
    print (x-y)
elif fn == "/":
    print (x/y)
elif fn == "*":
    print (x*y)
else:
    print("WTF you doing brah?!")

print("\nThat was easy!!")
